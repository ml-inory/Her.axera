def _spec_by_alias() -> dict[str, ModelSpec]:
    specs: dict[str, ModelSpec] = {}
    for spec in MODEL_SPECS:
        specs[spec.key] = spec
        for alias in spec.aliases:
            specs[alias] = spec
    return specs


def resolve_models(names: Iterable[str]) -> list[ModelSpec]:
    requested = [name.strip().lower() for name in names if name.strip()]
    if not requested or "all" in requested:
        return list(MODEL_SPECS)

    specs_by_alias = _spec_by_alias()
    selected: list[ModelSpec] = []
    seen: set[str] = set()
    for name in requested:
        spec = specs_by_alias.get(name)
        if spec is None:
            valid = ", ".join(["all", *(spec.key for spec in MODEL_SPECS)])
            raise SystemExit(f"Unknown model '{name}'. Valid values: {valid}")
        if spec.key not in seen:
            selected.append(spec)
            seen.add(spec.key)
    return selected


def render_env(specs: Iterable[ModelSpec], root: Path, endpoint: str) -> str:
    lines = [
        "# Generated by backend/tools/download_ax_models.py",
        f"HF_ENDPOINT={endpoint}",
        f"HER_AXERA_MODEL_ROOT={root}",
        "",
    ]
    for spec in specs:
        path = root / spec.local_dir
        lines.append(f"# {spec.key}: {spec.repo_id}")
        if spec.note:
            lines.append(f"# NOTE: {spec.note}")
        if not spec.env:
            lines.append("# No backend env vars are available for this model yet.")
        for key, template in spec.env:
            lines.append(f"{key}={template.format(root=root, path=path)}")
        lines.append("")
    return "\n".join(lines).rstrip() + "\n"


def download_models(
    specs: Iterable[ModelSpec],
    *,
    root: Path,
    endpoint: str,
    revision: str | None,
    token: str | None,
    max_workers: int,
    force_download: bool,
    dry_run: bool,
) -> None:
    from huggingface_hub import snapshot_download

    os.environ.setdefault("HF_ENDPOINT", endpoint)
    if not dry_run:
        root.mkdir(parents=True, exist_ok=True)
    for spec in specs:
        target = root / spec.local_dir
        print(f"[download] {spec.repo_id} -> {target}")
        result = snapshot_download(
            repo_id=spec.repo_id,
            repo_type="model",
            revision=revision,
            local_dir=target,
            endpoint=endpoint,
            token=token,
            max_workers=max_workers,
            force_download=force_download,
            dry_run=dry_run,
        )
        if dry_run:
            print(f"[dry-run] {spec.repo_id}: {len(result)} files")
        else:
            print(f"[done] {spec.key}: {result}")


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description="Download Her.axera AX model repositories on an AX board.")
    parser.add_argument(
        "models",
        nargs="*",
        help="Models to download: all, sensevoice, wenet, whisper, fireredasr, kokoro, zipvoice, speaker.",
    )
    parser.add_argument(
        "--root",
        type=Path,
        default=Path(os.getenv("HER_AXERA_MODEL_ROOT", DEFAULT_MODEL_ROOT)),
        help=f"Model root directory. Default: {DEFAULT_MODEL_ROOT}",
    )
    parser.add_argument(
        "--endpoint",
        default=os.getenv("HF_ENDPOINT", DEFAULT_ENDPOINT),
        help=f"Hugging Face endpoint. Default: {DEFAULT_ENDPOINT}",
    )
    parser.add_argument("--revision", default=None, help="Optional git revision, branch, or tag.")
    parser.add_argument("--token", default=os.getenv("HF_TOKEN"), help="Optional Hugging Face token.")
    parser.add_argument("--max-workers", type=int, default=4, help="Parallel download workers.")
    parser.add_argument("--force-download", action="store_true", help="Re-download files even when cached.")
    parser.add_argument("--dry-run", action="store_true", help="List files without downloading them.")
    parser.add_argument("--env-file", type=Path, help="Write backend env snippet to this file.")
    parser.add_argument("--print-env", action="store_true", help="Print backend env snippet after downloading.")
    return parser.parse_args()


def main() -> None:
    args = parse_args()
    specs = resolve_models(args.models)
    download_models(
        specs,
        root=args.root,
        endpoint=args.endpoint,
        revision=args.revision,
        token=args.token,
        max_workers=args.max_workers,
        force_download=args.force_download,
        dry_run=args.dry_run,
    )
    env_text = render_env(specs, args.root, args.endpoint)
    if args.env_file:
        args.env_file.parent.mkdir(parents=True, exist_ok=True)
        args.env_file.write_text(env_text, encoding="utf-8")
        print(f"[env] wrote {args.env_file}")
    if args.print_env:
        print(env_text, end="")


if __name__ == "__main__":
    main()

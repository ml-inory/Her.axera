#!/usr/bin/env python3
"""Download AXERA model repositories on an AX board.

The script intentionally uses huggingface_hub instead of git-lfs so boards can
resume downloads and use the hf-mirror endpoint by default.
"""

from __future__ import annotations

import argparse
import os
from dataclasses import dataclass
from pathlib import Path
from typing import Iterable


